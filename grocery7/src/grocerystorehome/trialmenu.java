    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            Connection connection = DriverManager. getConnection("jdbc:mysql://localhost:3306/dbms_proj","root","dbms123");
            System.out.println("Connected With the database successfully");
            PreparedStatement preparedStatement =connection.prepareStatement("insert into cart values(?,?,?,?)");
            
             String prod_name= jComboBox1.getSelectedItem().toString();
             String str= jTextField1.getText();
             int qty= Integer.parseInt(str);
             PreparedStatement preparedStatement2=connection.prepareStatement("select * from products where product_name='"+prod_name+"'");
             ResultSet rs= preparedStatement2.executeQuery();
             rs.next();
             int id=rs.getInt("idProducts");
             int price=rs.getInt("product_rate");
             int cost=qty*price;
             int prqty=rs.getInt("product_quantity");
             if(qty<=prqty){
              preparedStatement.setInt(1,qty);
              preparedStatement.setInt(2,cost);
              preparedStatement.setString(3,prod_name);
              preparedStatement.setInt(4,id);
                //Executing Query
                preparedStatement.executeUpdate();
                System.out.println("Data inserted Successfully");   
              PreparedStatement preparedStatement3=connection.prepareStatement("UPDATE products SET product_quantity="+(prqty-qty)+" where idProducts="+id);
              preparedStatement3.executeQuery();
             }else{
                 jLabel5.setText("Insufficient Stock.Cannot be added to cart");
             }
        }catch(SQLException e){
            System.out.println("Error while connecting to the database");
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/dbms_proj","root","dbms123");
            System.out.println("Connection to database successful");
            String productname = jTextField1.getText();
            int productcost = Integer.parseInt(jTextField2.getText());
            int productqty = Integer.parseInt(jTextField3.getText());
            PreparedStatement statement1 = conn.prepareStatement("insert into products(product_name,product_quantity,product_rate) values(?,?,?)");
            statement1.setString(1, productname);
            statement1.setInt(3, productcost);
            statement1.setInt(2, productqty);
            statement1.executeUpdate();
            System.out.println("Data has been inserted successfuly");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            
        }
        catch(Exception e){
            System.out.println("Error connecting to db");
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

